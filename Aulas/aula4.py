#Tipos int e float
#int -> número inteiro
#O tipo int representa qualquer número
#positivo ou negativo. Int sem sinal é considerado positivo
print(11)
print(-11)
print(0)
#float -> Número com ponto flutuante
#O tipo floar representa qualquer
#nu´mero positivo ou negativo com ponto flutuante
#float sem sinal e considerado positivo
print(1.1, 10.11)
print(0.0, -1.5)
#A função type mostra o tipo que o Python inferiu o valor
print(type("Teste"))
print(type(1))
print(type(1.1))