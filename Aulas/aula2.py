# Print é uma função em Python para imprimir informações na tela 
#Ele recebe argumentos dentro de sua função
#Podemos colocar mais de um argumentos separando eles por virgula
print(12, 34)
print(56, 78, sep="--") #Temos este argumento para definir o que queremos colocar entre dois argumentos, por padrão é " "
# A quebra de linha no computadores é representado por "\r\n"
#Perceba que usando o end podemos retinar a quebra de linha
print("Olá", end=" ")
print ("Mundo!")
#Assim é ele por padrão. 
print("haha", end="\r\n")
